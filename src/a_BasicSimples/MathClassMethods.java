package a_BasicSimples;

public class MathClassMethods {

	public static void main(String[] args) {
		System.out.println(Math.ceil(7.4)); 	//Uste Yuvarla
		System.out.println(Math.floor(7.4)); 	//Alta Yuvarla
		System.out.println(Math.max(8.6,5.2)); 	//Buyuk Olani Al
		System.out.println(Math.min(8.6,5.2)); 	//Kucuk Olani Al
		System.out.println(Math.pow(5,3)); 		//Ustunu Al
		System.out.println(Math.sqrt(9)); 		//Kokunu Al
	}
}
